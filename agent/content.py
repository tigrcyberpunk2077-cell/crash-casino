"""Контент-движок для каналов-персон.

Канал ведётся от лица персоны (девушка-эксперт) в своей нише (трейдинг / ставки
/ инвестиции и т.п.). Посты — от первого лица, образовательные, живые. Каждый
пост строится под случайный «угол», чтобы лента не была однообразной.

Подача — ответственная: образование, а не «сигналы» и обещания лёгких денег.
Это и этичнее, и сильно снижает риск блокировки канала/аккаунта.
"""

from __future__ import annotations

import random
from typing import Dict, List, Optional, Tuple

from .config import Config
from .llm import generate_post

# Углы подачи: имя + о чём именно пост. Подходят и для трейдинга, и для ставок.
ANGLES: List[Dict[str, str]] = [
    {"name": "beginner_tip", "brief": "Один практический совет новичку — конкретный и применимый сегодня."},
    {"name": "term_explainer", "brief": "Объясни простыми словами один термин/инструмент из ниши."},
    {"name": "myth", "brief": "Разбери частый миф или ошибку новичков и как делать правильно."},
    {"name": "psychology", "brief": "Психология и дисциплина: эмоции, жадность, управление собой."},
    {"name": "risk", "brief": "Управление капиталом/банком и риском — почему это важнее «угадал/не угадал»."},
    {"name": "checklist", "brief": "Короткий чек-лист/правила перед сделкой или ставкой."},
    {"name": "qa", "brief": "Ответ на частый вопрос подписчиков (сформулируй вопрос и ответь)."},
    {"name": "mindset_story", "brief": "Личная мотивирующая зарисовка из будней персоны (без выдуманных пруфов прибыли)."},
    {"name": "market_note", "brief": "Образовательный разбор подхода к ситуации на рынке/в матчах — БЕЗ конкретных сигналов «делай X сейчас»."},
    {"name": "engage", "brief": "Вовлекающий вопрос подписчикам + мини-польза по теме."},
]


def pick_angle(exclude: Optional[str] = None) -> Dict[str, str]:
    pool = [a for a in ANGLES if a["name"] != exclude] or ANGLES
    return random.choice(pool)


def _system(persona: str, niche: str) -> str:
    return (
        "Ты — автор личного Telegram-канала, который ведёт от первого лица живой человек. "
        "Твои посты реально хочется читать и дочитывать.\n"
        f"Персона: {persona}\n"
        f"Ниша канала: {niche}\n\n"
        "ГЛАВНОЕ — ПОСТ ДОЛЖЕН ЦЕПЛЯТЬ:\n"
        "• Первая строка решает всё. Это крючок, который останавливает листание: неожиданное "
        "заявление, конкретная цифра, интрига, мини-история «вот что со мной было» или дерзкий "
        "вопрос. НИКАКИХ «Привет, друзья!» и общих вступлений — сразу в суть.\n"
        "• Одна мысль на пост, раскрытая ярко. Лучше одна живая история/пример, чем список общих советов.\n"
        "• Конкретика вместо воды: цифры, детали, реальные ситуации. Не «управляй рисками», а "
        "«я рискую максимум 2% депозита в сделке — и вот почему однажды это спасло мне месяц».\n"
        "• Дай эмоцию или инсайт: удивление, узнавание себя, «о, а так можно было».\n"
        "• Финал — сильный: вопрос или мысль, на которую хочется ответить в комментариях.\n\n"
        "ЖИВОЙ ЯЗЫК (чтобы не читалось как ИИ):\n"
        "• Разговорно, как другу. Короткие фразы, можно неполные. Личные обороты: «честно», "
        "«знаю по себе», «короче». Варьируй длину постов и то, как начинаешь.\n"
        "• Эмодзи — по смыслу, где усиливают мысль или заменяют слово (обычно 1–4 на пост), "
        "не в каждой строке и не для украшательства. Без хэштегов.\n"
        "• Первая строка — это заголовок-крючок (он будет выделен жирным). При желании можешь "
        "выделить ещё одну ключевую фразу, обернув её в **двойные звёздочки**. Другой разметки нет.\n"
        "• БЕЗ штампов и канцелярита: «Итак», «В современном мире», «Важно понимать», «Давайте "
        "разберёмся», «подытожим».\n\n"
        "ЧЕСТНОСТЬ:\n"
        "• Это образование, не реклама. Без обещаний гарантированной прибыли и «лёгких денег», "
        "без «сигналов покупай сейчас» и выдуманных скринов прибыли. Про риск — живыми словами.\n\n"
        "Объём — до ~140 слов. Верни ТОЛЬКО текст поста — без пояснений, без кавычек."
    )


def _prompt(niche: str, about: str, recent: List[str], cta_link: str) -> str:
    lines = [
        f"Тема канала: {niche}.",
        f"Этот пост — ИМЕННО про: {about}. Держись этой темы, не уходи в сторону.",
        "Сделай его цепляющим по правилам выше: сильный крючок в первой строке, "
        "одна мысль, конкретика, живой финал.",
    ]
    if cta_link:
        lines.append(
            f"В конце уместно добавить мягкий призыв со ссылкой: {cta_link} "
            "(не навязчиво, по-человечески)."
        )
    else:
        lines.append(
            "В конце — вовлечение: вопрос подписчикам, призыв написать в комментариях "
            "или поставить реакцию (без внешних ссылок)."
        )
    if recent:
        snippets = "\n".join(f"— {t[:120]}" for t in recent)
        lines.append("Не повторяй по смыслу и формулировкам недавние посты:\n" + snippets)
    lines.append("Напиши один новый пост от лица персоны.")
    return "\n".join(lines)


async def make_draft(
    cfg: Config,
    niche: str,
    persona: str,
    recent: List[str],
    exclude_angle: Optional[str] = None,
    brief: Optional[str] = None,
) -> Tuple[str, str]:
    """Возвращает (текст_поста, метка_угла).

    brief — «про что» пост (например, подпись к присланному фото). Если задан,
    пишем именно об этом; иначе берём случайный угол подачи.
    """
    persona = persona or cfg.default_persona
    niche = niche or cfg.default_niche
    if brief:
        about, angle_name = brief, "по запросу"
    else:
        angle = pick_angle(exclude=exclude_angle)
        about, angle_name = angle["brief"], angle["name"]
    text = await generate_post(cfg, _system(persona, niche), _prompt(niche, about, recent, cfg.cta_link))
    return text, angle_name


def _reel_system(persona: str, niche: str) -> str:
    return (
        "Ты пишешь сценарий короткого вертикального видео (Reels/TikTok) от лица персоны. "
        "Текст будет ОЗВУЧЕН голосом и показан субтитрами на экране.\n"
        f"Персона: {persona}\n"
        f"Ниша: {niche}\n"
        "Правила:\n"
        "• Это устная речь — пиши так, как говорят вслух.\n"
        "• БЕЗ эмодзи, смайликов, хэштегов, markdown и спецсимволов (их зачитает голос).\n"
        "• Коротко: ~25–35 секунд, примерно 55–85 слов. Не растягивай.\n"
        "• Первая фраза (первые 3 секунды) — мощный хук, чтобы не пролистнули.\n"
        "• Одна мысль, конкретно, живым языком, с примером или цифрой.\n"
        "• В конце — короткий призыв: подпишись / сохрани / напиши в комментариях.\n"
        "• Образование, без обещаний прибыли, без «сигналов».\n"
        "Верни ТОЛЬКО текст, который произносится — без пометок «хук», «сцена», без кавычек."
    )


async def make_reel(
    cfg: Config, niche: str, persona: str, brief: Optional[str] = None,
    exclude_angle: Optional[str] = None,
) -> str:
    """Сценарий короткого видео (озвучка + субтитры). Возвращает текст реплики."""
    persona = persona or cfg.default_persona
    niche = niche or cfg.default_niche
    about = brief or pick_angle(exclude=exclude_angle)["brief"]
    user = (
        f"Тема ролика: {niche}.\n"
        f"Этот ролик — про: {about}. Держись темы.\n"
        "Сделай цепляющий хук в первой фразе и сильный финал-призыв."
    )
    return (await generate_post(cfg, _reel_system(persona, niche), user)).strip()
